$(document).ready(function () {
    var currentDate = new Date();

    var dateTimeString = currentDate.toLocaleString('en-US', {
        weekday: 'long',
        year: 'numeric',
        month: 'long',
        day: 'numeric',
        hour: 'numeric',
        minute: 'numeric',
        second: 'numeric',
        timeZoneName: 'short'
    });
    $('#datetime').text(dateTimeString);
});